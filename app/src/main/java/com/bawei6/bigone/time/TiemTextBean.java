package com.bawei6.bigone.time;

/**
 * @author fengchen
 * @date 2020/1/2.
 * @description：时间轴的Bean
 */
public class TiemTextBean {
    String one,two,three;

    public TiemTextBean(String one, String two, String three) {
        this.one = one;
        this.two = two;
        this.three = three;
    }

    public String getOne() {
        return one;
    }

    public void setOne(String one) {
        this.one = one;
    }

    public String getTwo() {
        return two;
    }

    public void setTwo(String two) {
        this.two = two;
    }

    public String getThree() {
        return three;
    }

    public void setThree(String three) {
        this.three = three;
    }
}
